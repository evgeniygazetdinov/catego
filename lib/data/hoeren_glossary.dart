/// Нормализация слова для поиска в [kHoerenGlossary].
String normalizeGermanToken(String raw) {
  final s = raw.trim().toLowerCase();
  if (RegExp(r'^\d+:\d+$').hasMatch(s)) return s;
  if (RegExp(r'^\d+[.,]\d+$').hasMatch(s)) return s;
  return s
      .replaceAll(RegExp(r'^[^a-zäöü0-9]+'), '')
      .replaceAll(RegExp(r'[^a-zäöü0-9]+$'), '');
}

/// Перевод: общий словарь + подсказки к заданию (перекрывают общий).
String? lookupHoerenWord(String surface, {Map<String, String>? wordHints}) {
  final key = normalizeGermanToken(surface);
  if (key.isEmpty) return null;
  final h = wordHints;
  if (h != null && h.containsKey(key)) {
    return h[key];
  }
  return kHoerenGlossary[key] ?? kHoerenGlossarySupplement[key];
}
